#include <iostream>

using namespace std;

int main()
{
    std::cout << "Abszolut ertekek:"<<endl<<"A szamod: ";
    int a;
    cin >>a;
    if (a == 0) {
        cout << "NO!";
        return 0;
    }
    else if (a > 0) {
        cout << "Abszolut erteke: " << a<<endl;
        if (a % 2 == 0) {
            cout << "A fele: " << a / 2;
        }
    }
    else if (a < 0) {
        cout << "Abszolut erteke: " << a * (-1)<<endl;
        if (a % 2 == 0) {
            cout << "A fele: " << a / 2;
        }
    }

}

